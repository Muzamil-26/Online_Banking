                    <h1>Common Signs of Fake Websites</h1>
                    <p>Here are some indicators that a website may be fake:</p>
                    <div class="lists">
                        <ol>
                            <li><b>Unusual URL:</b> Check the website's URL carefully. Fake sites often use misspellings or different domain extensions.</li>
                            <li><b>Lack of HTTPS:</b> Secure websites use HTTPS. If the site lacks this, it may not be safe.</li>
                            <li><b>Poor Design:</b> Fake websites often have poor design, low-quality images, and numerous typographical errors.</li>
                            <li><b>Excessive Pop-ups:</b> Be wary of sites that bombard you with pop-up ads or requests for personal information.</li>
                            <li><b>Missing Contact Information:</b> Legitimate businesses provide clear contact details. If this information is missing, it could be a red flag.</li>
                        </ol>
                    </div>
