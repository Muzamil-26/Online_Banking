                        <li><b>Phishing Emails:</b> Scammers may send emails claiming to be from legitimate companies, urging recipients to call a number or click a link for support.</li>
                        <li><b>Remote Access:</b> Once they gain the victim's trust, scammers often ask for remote access to the computer to "fix" the problem, allowing them to install malware or steal personal information.</li>
