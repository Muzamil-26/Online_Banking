                        <ol>
                            <li><b>Card Skimming:</b> This involves the use of a small device to capture your card information when you use an ATM or payment terminal.</li>
                            <li><b>Phishing Scams:</b> Scammers send fake emails or messages pretending to be from your bank, asking for your card details.</li>
                            <li><b>Account Takeover:</b> Fraudsters gain access to your bank account using stolen personal information, allowing them to make unauthorized transactions.</li>
                            <li><b>Card Not Present Fraud:</b> This occurs when a scammer makes online purchases using your card information without your knowledge.</li>
                            <li><b>Fake Charity Scams:</b> Scammers may solicit donations for fake charities, using your card information to steal funds.</li>
