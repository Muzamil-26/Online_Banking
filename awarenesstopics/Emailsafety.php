                    <h1>Possible Threats</h1>
                    <p>
                        There are several threats to email security that one should be aware of:</p>
                    <div class="lists">
                        <ol>
                            <li> <b>Phishing attacks:</b> These are emails that try to trick you into giving away
                                sensitive
                                information,
                                such as your login credentials or financial information. They often appear to be
                                legitimate, but
                                they are actually sent by hackers trying to steal your information.</li>
                            <li> <b>Malware:</b> Malware is software that is designed to damage or disrupt computer
                                systems. It
                                can be
                                transmitted via email, and it can infect your computer when you open an attachment or
                                click on a
                                link in an email.</li>
                            <li><b>Ransomware:</b> Ransomware is a type of malware that encrypts your files and holds
                                them
                                hostage
                                until you pay a ransom. It can be transmitted via email and can cause significant damage
                                to your
                                computer and data.</li>
                            <li><b>Spam:</b>Spam emails are unwanted emails that are often sent in large quantities.
                                They can
                                clog up
                                your inbox and potentially contain malware or phishing attacks.</li>
                            <li><b>Man-in-the-middle attacks:</b> These are attacks where a hacker intercepts
                                communication
                                between two
                                parties in order to steal sensitive information or alter the communication. They can
                                happen when
                                you click on a link in an email and are redirected to a fake login page.</li>
                        </ol>
                    </div>
